// Widen literal string types to `string` so other locale files can use different values,
// while preserving the nested object shape.
type DeepStringify<T> = T extends readonly (infer U)[]
  ? readonly DeepStringify<U>[]
  : T extends object
    ? { readonly [K in keyof T]: DeepStringify<T[K]> }
    : T extends string
      ? string
      : T;

